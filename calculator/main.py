def add(a, b):
    """Compute and return sum of two numbers
    
    Examples: 
        >>> from calculator import main
        >>> main.add(5,4)
    Args:
        a (float): First number
        b (float): Second number

    Returns:
        Sum of a+b
    """
    return float(a+b)

def substract(a, b):
    """Compute and return rest of two numbers"""
    return float(a-b)
